function onSpeak(chat, msg)
	chat:SendYellowMessage(Self.Name(), msg)
	 cmd = {}
     for i in string.gmatch(msg, "%a+") do
       table.insert(cmd, i)
     end
	
	if cmd[1] == "help" then
		chat:SendOrangeMessage("Commands","\nreset - Resets the HUD\nss - Screenshot\nadd loot ID - Add ID to the loot list\nadd supply ID - add ID to the supplies list")
	elseif cmd[1] == "ss" then
		screenshot(Self.Name() .. "_" .. os.date("%H.%M.%S"))
	elseif cmd[1] == "reset" then
		SessionStart = os.time()
		ExperienceStart = Self.Experience()		
		for i=1, #LootIDs do
			LootIDs[i].samount = 0
			LootIDs[i].vamount = 0
		end
		for i=1, #SuppliesIDs do
			SuppliesIDs[i].samount = 0
			SuppliesIDs[i].vamount = 0
			SuppliesIDs[i].uamount = 0
			SuppliesIDs[i].mamount = 0
		end
	elseif cmd[1] == "start" then
		HUDUpdateStart = true
	elseif cmd[1] == "add" then
		for i in string.gmatch(msg, "%d+") do
			if cmd[2] == "loot" then
				LootIDs[#LootIDs + 1] = { id = tonumber(i), vamount = 0, samount = 0, hud = false}
			elseif cmd[2] == "supply" then
				SuppliesIDs[#SuppliesIDs + 1] = { id = tonumber(i), vamount = 0, samount = 0, mamount = 0, uamount = 0, hud = false}
				chat:SendOrangeMessage("NakuuHUD", Item.GetName(tonumber(i)).." added")
			end
		end
	elseif cmd[1] == "pause" then
		HUDUpdateStart = false
	else
		chat:SendOrangeMessage("NakuuHUD", "Unknown command. Type 'help' for command list.")
	end
end

