function table.binaryfind(self, v, a)
	if a then
		local l, r = 1, #self
		while l <= r do
			local m = math.floor((r + l) / 2)
			if self[m][a] == v then
				return m
			end
			if self[m][a] > v then
				r = m - 1
			else
				l = m + 1
			end
		end
	else
		local l, r = 1, #self
		while l <= r do
			local m = math.floor((r + l) / 2)
			if self[m] == v then
				return m
			end
			if self[m] > v then
				r = m - 1
			else
				l = m + 1
			end
		end
	end
	return nil
end

function GetItemInfo(item)
	local i
	i = ITEM_INFORMATION[table.binaryfind(ITEM_INFORMATION, item, 'id')]
	return i or {id = 0, name = "unknown", weight = 0, value = 0, price = 0}
end

function ItemValue(item)
	return GetItemInfo(item).value
end

function ItemPrice(item)
	return GetItemInfo(item).price
end

Map.GetUseItems = function (id)
  if type(id) == "string" then
    id = Item.GetID(id)
  end
  local pos = Self.Position()
  local store = {}
  for x = -7, 7 do
    for y = -5, 5 do
      if Map.GetTopUseItem(pos.x + x, pos.y + y, pos.z).id == id then
        itemPos = {x = pos.x + x, y = pos.y + y, z = pos.z}
        table.insert(store, itemPos)
      end
    end
  end
  return store
